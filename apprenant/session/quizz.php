						finite[ 'success' ] = true;
					}
					else if ( finite[ 'score' ] == 9 ) {
						finite[ 'verb' ] = "mastered";
						finite[ 'success' ] = true;
					}
					else {
						finite[ 'verb' ] = "completed";
						finite[ 'success' ] = true;
					}

